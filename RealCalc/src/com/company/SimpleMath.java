package com.company;

public class SimpleMath {

    public SimpleMath(){
    }


    public float addXY (float x, float y) { return y+x; }
    public float subXY (float x, float y) {
        return y-x;
    }
    public float divideXY (float x, float y) {return y/x;}
    public float multiXY (float x, float y) {return y*x;}

}
